namespace App\Controller;

use Cake\Controller\Controller;
use Cake\Event\Event;

/**
 * Application Controller
 *
 * Add your application-wide methods in the class below, your controllers
 * will inherit them.
 *
 * @link http://book.cakephp.org/3.0/en/controllers.html#the-app-controller
 */
class AppController extends Controller
{

    /**
     * Initialization hook method.
     *
     * Use this method to add common initialization code like loading components.
     *
     * e.g. `$this->loadComponent('Security');`
     *
     * @return void
     */
    public function initialize()
    {
        parent::initialize();

        $this->loadComponent('RequestHandler');
        $this->loadComponent('Flash');
        $this->loadComponent('Auth', [
            'loginRedirect' => [
                'controller' => 'Admin',
                'action' => 'index'
            ],
            'logoutRedirect' => [
                'controller' => 'Pages',
                'action' => 'display',
                'home'
            ]
        ]);
    }

    public function beforeFilter(Event $event)
    {
        $this->Auth->authenticate = array( 'Basic' => array('userModel' => 'Admin'), 'Form' => array('userModel' => 'Admin') );
       /* $this->Auth->userModel = 'Admin';
        $this->Auth->loginAction = array('controller' => 'admin', 'action' => 'login');
        $this->Auth->loginRedirect = array('controller' => 'admin', 'action' => 'index');
        $this->Auth->logoutRedirect = array('controller' => 'admin', 'action' => 'logout');
        $this->Auth->authorize = 'controller';
        $this->Auth->userScope = array('Admin.active' => 1);*/

        //$this->Session->write('Auth.redirect','/admin/index');
        $this->Auth->allow(['index', 'view', 'display']);
    }

    /**
     * Before render callback.
     *
     * @param \Cake\Event\Event $event The beforeRender event.
     * @return void
     */
    public function beforeRender(Event $event)
    {
        if (!array_key_exists('_serialize', $this->viewVars) &&
            in_array($this->response->type(), ['application/json', 'application/xml'])
        ) {
            $this->set('_serialize', true);
        }
    }
}
